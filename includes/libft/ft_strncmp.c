#include "libft.h"

int		ft_strncmp(char *s1, char *s2, int len)
{
	int i;

	i = 0;
	while (i < len)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
